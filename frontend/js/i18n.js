let currentLang = localStorage.getItem('retail-lang') || 'id';

export function setLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('retail-lang', lang);
    applyTranslations();
}

export function __(key, fallback = '') {
    const dict = translations[currentLang] || translations.id;
    return dict[key] || fallback || key;
}

export function applyTranslations() {
    const lang = localStorage.getItem('retail-lang') || 'id';
    currentLang = lang;
    const dict = translations[lang] || translations.id;

    // Update html lang attribute
    document.documentElement.setAttribute('lang', lang === 'en' ? 'en' : 'id');

    // Translate all elements with data-i18n
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (dict[key]) {
            if (el.tagName === 'INPUT' && el.type === 'text') {
                el.placeholder = dict[key];
            } else if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.placeholder = dict[key];
            } else {
                // Check if element has child text node or just use textContent
                let hasTextNode = false;
                el.childNodes.forEach(node => {
                    if (node.nodeType === Node.TEXT_NODE && node.nodeValue.trim().length > 0) {
                        node.nodeValue = dict[key];
                        hasTextNode = true;
                    }
                });
                if (!hasTextNode) el.textContent = dict[key];
            }
        }
    });

    // Translate aria-labels
    document.querySelectorAll('[data-i18n-aria]').forEach(el => {
        const key = el.getAttribute('data-i18n-aria');
        if (dict[key]) el.setAttribute('aria-label', dict[key]);
    });

    // Dispatch event for views to react to language change
    document.dispatchEvent(new CustomEvent('languageChanged', { detail: { lang } }));
}

export function initI18n() {
    applyTranslations();
}
